package fr.univ_amu.iut.beans;

public class Ennemi {

    private int idEnnemi;
    private int name;
    private int hp;
    private int dmg;
    private int def;


    public int getIdEnnemi() {
        return idEnnemi;
    }

    public void setIdEnnemi(int idEnnemi) {
        this.idEnnemi = idEnnemi;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getDmg() {
        return dmg;
    }

    public void setDmg(int dmg) {
        this.dmg = dmg;
    }

    public int getDef() {
        return def;
    }

    public void setDef(int def) {
        this.def = def;
    }
}
